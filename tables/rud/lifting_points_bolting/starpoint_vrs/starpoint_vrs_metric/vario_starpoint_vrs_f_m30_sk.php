					<h4>Description</h4>
					<p>Star shaped design, a clear distinction from the DIN 580 eye bolt, with a striking fluorescent pink powder coating.</p>
					<p>Identification: clear WLL indication for the most unfavourable load direction F (not permissible for DIN 580). Forged material 1.6541, high tensile and annealed, 100% electromagnetic crack detected according to EN 1677-1 and with a 4:1 design factor.</p>
					<p>Choose the attachment point in such a way that the introduced forces are accommodated by the work piece without being deformed. Make sure the bolting surface is plain. Ensure an appropriate bore depth to facilitate an optimal sitting of the engaging surface. Work piece material, at least 1.0037 (St 37).</p>
					<p>For light metals, non-ferrous heavy metals, and grey cast iron, the thread type allocation must be done in such a way that the WLL of the thread type fulfills the requirements of the respective work piece.<br />
					  Without an additional washer, screw the VRS into the work piece until the seat-engaging surface of the hexagon socket bolt evenly lies on the surface of the work piece. Is the VRS to remain permanently at the point of force introduction, it must be tightened with a torque of 330 nm.</p>
					<p>The thread bore countersink is equal to the effective diameter of the thread. Always pay attention that the hexagon socket bolt is tightened. When impact loaded or operated in environments with skewing and vibrations, unintentional loosening can happen, especially if through bolts with nuts are used.</p>
					<p>Securing possibilities: use of a liquid thread securing medium, e.g. Loctite (follow the manufacturer's recommendations). In a tightened condition, the VRS must be able to swivel up to 360&deg;. Before loading, adjust the VRS to the direction of the applied force. A captive hexagon socket bolt.</p>
					<p>The VRS is patent protected: GM 9402014 &ndash; and further industrial property rights are registered under GM 9316475.</p>
					<p>For the tool-less assembling, the STARPOINT is supplied with an annealed star-profile-key. Simply engage the hexagon socket bolt with the star-profile-key and use your fingers to respectively tighten or untighten the arrangement. Disengage the key. The VRS is rotatable.</p></td>
