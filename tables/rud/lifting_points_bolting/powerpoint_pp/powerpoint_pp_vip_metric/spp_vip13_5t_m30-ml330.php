			<h4>Description</h4>
	          <p>Thread reach from F= 330 mm is available.</p>
	          <p>When ordering implicitly indicate the required length.</p>
	          <p>WLL is equivalent to that of WBG-V, since a corresponding lower part is used.</p>
	          <p>Connection only with RUD approved components, e.g., VCÖH-13, oval link, PP 5000 kg or the 13 mm VIP chain.</p>
	          <p>Hint: The VIP chain connection is fool proof. When assembling the components, pay attention to the correct WLL allocation.</p>
	          <p>Choose the attachment points in such a way that the introduced forces are accommodated by the work piece without being deformed. Work piece material, at least 1.0037 (St 37).</p>
	          <p>The BG recommends the following min. thread lengths:<br>
	            1 x M for Steel<br>
	            1.25 x M for Cast<br>
	            2 x M for Aluminium</p>
	          <p>For light metals, non-ferrous heavy metals, and grey cast iron, the thread type allocation must be done in such a way that the WLL of the thread type fulfills the requirements of the respective work piece.</p>
	          <p>Even under full load, can be turned in a 90° position from the bolt centre line. Not suitable for permanent swiveling under full load. Make sure the bolting surface is plain.</p>
	          <p>Ensure an appropriate bore depth to facilitate an optimal sitting of the engaging surface. For a single transportation procedure, bolting in and fastening tightly with a flat wrench is enough.</p>
	          <p>Without a washer, the seat-engaging surface of the Power point (D) must completely lie onto the surface of the work piece. Is the Power point to be permanently loaded, it must be tightened with a torque of 225 nm.</p>
	          <p>When impact loaded or operated in environments with skewing and vibrations, unintentional loosening can happen, especially if through bolts with nuts are used.</p>
              <p>Securing possibilities: Use of a liquid thread securing medium, e.g. Loctite (follow the manufacturer's recommendations), or positive bolt securing, for example, using a castellated nut with a split pin, a counter nut, etc.</p></td>
